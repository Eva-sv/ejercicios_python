# Ask the user to enter 2 numbers
# Convert to number. int (no decimals), float (can have decimals)

numero1 = float(input("Please enter a number"))
numero2 = float(input("Please enter another number"))

# We perform the operations

addition = numero1 + numero2
subtraction = numero1 - numero2
multiplication = numero1 * numero2
division = numero1 / numero2

# We print the results

print("Addition", addition)
print("Subtraction", subtraction)
print("Multiplication", multiplication)
print("Division", division)


